    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int empID = Integer.valueOf(TextEmpID.getText());
        String nombre = TextNombre.getText();
        String empTipo = TextEmpTipo.getText();
        String password = TextPassword.getText();
        
        Empleado empleado = new Empleado(empID,nombre, empTipo, password);
        
        try {
            DaoEmpleado.insertar(empleado);
            IngresoCorrecto exito = new IngresoCorrecto();
            exito.setVisible(true);
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(NuevoEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        IngresoCorrecto ingresoCorrecto = new IngresoCorrecto();
        ingresoCorrecto.setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

