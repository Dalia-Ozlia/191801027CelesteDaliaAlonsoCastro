    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ingresoID = Integer.valueOf(TextIngresoID.getText());
        switch (operacion){
            case 1:
                Empleado empleado = new Empleado(ingresoID);
                
                try {
                    DaoEmpleado.eliminar(empleado);
                } catch (SQLException ex) {
                    Logger.getLogger(Eliminar.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            case 2:
                Herramienta herramienta = new Herramienta(ingresoID);
            
                try {
                    DaoHerramienta.eliminar(herramienta);
                } catch (SQLException ex) {
                    Logger.getLogger(Eliminar.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            case 3:
                Mantenimiento mantenimiento = new Mantenimiento(ingresoID);
                
                try {
                    DaoMantenimiento.eliminar(mantenimiento);
                } catch (SQLException ex) {
                    Logger.getLogger(Eliminar.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;
            case 4:
                Refaccion refaccion = new Refaccion(ingresoID);

                try {
                    DaoRefaccion.eliminar(refaccion);
                } catch (SQLException ex) {
                    Logger.getLogger(Eliminar.class.getName()).log(Level.SEVERE, null, ex);
                }
                break;

        }
        EliminacionCorrecta eliminacionCorrecta = new EliminacionCorrecta();
        eliminacionCorrecta.setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

